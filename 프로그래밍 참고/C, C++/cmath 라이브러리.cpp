#include <iostream>
#include <cmath>
using namespace std;

int main() {
	
	float e  = 2.718281;

	cout << ceil(e) << endl;  // 소수점 첫번째 자리에서 올림
	cout << floor(e) << endl; // 소수점 첫번재 자리에서 내림

	cout << pow(3, 2) << endl; // 3의 2제곱 반환
	cout << sqrt(9) << endl;   // 9의 제곱근 반환

	return 0;
}